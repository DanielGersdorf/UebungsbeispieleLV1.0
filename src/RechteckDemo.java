public class RechteckDemo {
    public static void main(String[] args) {
        int breite;
        int laenge;
        int umfang;
        int flaeche;
        breite = 4;
        laenge = 6;

        umfang = 2 * breite + 2 * laenge;
        flaeche = breite * laenge;
        System.out.println("Umfang:"+ umfang);
        System.out.println("Flaeche = " + flaeche);
    }

    }
